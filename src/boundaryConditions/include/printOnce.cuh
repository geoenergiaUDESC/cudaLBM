Description
    Helper for printing each boundary fallback only once during execution 

Namespace
    LBM

SourceFiles
    printOnce.cuh

Notes
    Intended to be called within a case block inside a boundary switch statement.

\*---------------------------------------------------------------------------*/

#ifndef __MLBM_PRINTONCE_CUH
#define __MLBM_PRINTONCE_CUH

namespace LBM
{
    __device__ int printedFallback[26] = {false};

    __device__ inline void printOnce(const uint8_t caseId, const char* name)
    {
        if (atomicExch(&printedFallback[caseId], true) == false)
            printf("[fallback] %s applied\n", name);
    }
}

#endif
